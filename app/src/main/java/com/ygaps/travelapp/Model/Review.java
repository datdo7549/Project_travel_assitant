package com.ygaps.travelapp.Model;

public class Review {
    private int id;
    private String name;
    private String review;
    private String avatar;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getReview() {
        return review;
    }

    public String getAvatar() {
        return avatar;
    }
}

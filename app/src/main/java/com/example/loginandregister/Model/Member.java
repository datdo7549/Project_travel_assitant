package com.example.loginandregister.Model;

public class Member {
    private int id;
    private String name;
    private String phone;
    private String avatar;
    private Boolean isHost;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getPhone() {
        return phone;
    }

    public String getAvatar() {
        return avatar;
    }

    public Boolean getHost() {
        return isHost;
    }
}
